package com.company.employeeinfomodel;

public class EmployeeInfoModel {

	private String employeeName;
	private String employeeDesignation;
	private String employeeGrade;
	private String employeeSalary;
	private String employeeSalaryInfo;
	private String employeeSalaryDetails;

//	Getters And Setters

	public String getEmployeeName() {
		return employeeName;
	}

	public void setEmployeeName(String employeeName) {
		this.employeeName = employeeName;
	}

	public String getEmployeeDesignation() {
		return employeeDesignation;
	}

	public void setEmployeeDesignation(String employeeDesignation) {
		this.employeeDesignation = employeeDesignation;
	}

	public String getEmployeeGrade() {
		return employeeGrade;
	}

	public void setEmployeeGrade(String employeeGrade) {
		this.employeeGrade = employeeGrade;
	}

	public String getEmployeeSalary() {
		return employeeSalary;
	}

	public void setEmployeeSalary(String employeeSalary) {
		this.employeeSalary = employeeSalary;
	}

	public String getEmployeeSalaryInfo() {
		return employeeSalaryInfo;
	}

	public void setEmployeeSalaryInfo(String employeeSalaryInfo) {
		this.employeeSalaryInfo = employeeSalaryInfo;
	}

	public String getEmployeeSalaryDetails() {
		return employeeSalaryDetails;
	}

	public void setEmployeeSalaryDetails(String employeeSalaryDetails) {
		this.employeeSalaryDetails = employeeSalaryDetails;
	}

}
